
class Person():
    def __init__(self):
        pass

    def set_name(self, name):
        self.name = name
        
    def get_name(self):
        return self.name

    def set_age(self, age):
        self.age = age
        
    def get_age(self):
        return self.age

    def set_height(self, height):
        self.height = height
        
    def get_height(self):
        return self.height

    def set_weight(self, weight):
        self.weight = weight
        
    def get_weight(self):
        return self.weight

        